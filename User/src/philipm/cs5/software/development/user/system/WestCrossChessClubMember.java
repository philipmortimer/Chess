    /**
     * Loads contact details onto the appropriate forms
     */
    private void attemptToLoadContactInfo(){
        try{
            String[]contactInfo=ThreadClient.getChessClubContactDetails();//loads contact information from the server
            phoneNumberTxt.setText(contactInfo[0]);emailTxt.setText(contactInfo[1]);//sets the contact information on the relevant fields
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"An error occurred when attempting to get the club contact information from the server. Error: "+e,"Error",JOptionPane.OK_OPTION);
            error=true;//flags the fact that an error has occurred
            if(isFormInitialised){
                Menu menu = new Menu(session);
                menu.setVisible(true);this.dispose();
            }
        }
    }
