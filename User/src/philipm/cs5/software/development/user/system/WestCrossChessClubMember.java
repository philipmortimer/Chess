    private void leaveClubBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_leaveClubBtnActionPerformed
        // lets the user leave the chess club after confirming they wish to do so
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you would like to leave the chess club?", "Quit?", JOptionPane.YES_NO_CANCEL_OPTION);
        if(confirm==JOptionPane.YES_OPTION){
            //removes user from the chess club
            try{
                ThreadClient.removeUserFromChessClub(session.getUsername());
                session.setIsClubMember(false);
                Menu menu = new Menu(session);menu.setVisible(true);this.dispose();
                return;
            }catch(Exception e){
                JOptionPane.showMessageDialog(this,"Error when communicating with the server. "+e,"Error",JOptionPane.OK_OPTION);
                Menu menu = new Menu(session);menu.setVisible(true);this.dispose();
                return;
            }
        }else{
            return;
        }   
    }//GEN-LAST:event_leaveClubBtnActionPerformed

    private void viewLeagueTableBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewLeagueTableBtnActionPerformed
        //takes the user to a form to view the current league table
        try{
            String[][]leagueTable=ThreadClient.getLeagueTable();//requests the league table from the server
            if(leagueTable.length==0){
                //if the no fixtures have been generated, then the league table requires all the chess club members as an input instead
                String[]allPlayers=ThreadClient.getMembersOfChessClub();
                LeagueTable tab = new LeagueTable(session, allPlayers);
                tab.setVisible(true);this.dispose();return;
            }
            LeagueTable tab = new LeagueTable(session, leagueTable);
            tab.setVisible(true);this.dispose();return;
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Error when communicating with the server: "+e,"Error",JOptionPane.OK_OPTION);
            return;
        }
    }//GEN-LAST:event_viewLeagueTableBtnActionPerformed

