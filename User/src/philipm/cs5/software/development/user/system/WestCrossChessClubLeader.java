    private void attemptToLoadContactInfo(){
        try{
            String[]contactInfo=ThreadClient.getChessClubContactDetails();//gets contact details
            phoneNumberTxt.setText(contactInfo[0]);emailTxt.setText(contactInfo[1]);//loads contact details onto form 
            phoneNumber=contactInfo[0];email=contactInfo[1];//stores contact details
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"An error occurred when attempting to get the club contact information from the server. Error: "+e,"Error",JOptionPane.OK_OPTION);
            error=true;//shows that an error has occurred and goes to the menu if appropriate
            if(isFormInitialised){
                Menu menu = new Menu(session);
                menu.setVisible(true);this.dispose();
            }
        }
    }
