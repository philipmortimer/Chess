    private void changeContactDetailsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeContactDetailsBtnActionPerformed
        //attempts to change the contact details or save changes to contact details
        if(areContactDetailsBeingEdited){
            //attempts to save changes to server
            //updates form to reflect the fact that the contact details are no longer activel being edited
            areContactDetailsBeingEdited=false;
            cancelChangesBtn.setVisible(false);cancelChangesBtn.setEnabled(false);
            changeContactDetailsBtn.setText("Change Contact Details");
            phoneNumberTxt.setEditable(false);
            emailTxt.setEditable(false);
            try{
                ThreadClient.setChessClubContactDetails(phoneNumberTxt.getText(), emailTxt.getText());//saves changes
            }catch(Exception e){
                JOptionPane.showMessageDialog(this,"Error when communicating with the server: "+e,"Error",JOptionPane.OK_OPTION);
                Menu menu = new Menu(session);menu.setVisible(true);this.dispose();return;
            }
            phoneNumber=phoneNumberTxt.getText();email=emailTxt.getText();
        }else{//sets it so that the leader can edit the contact details
            //updates the form to allow the contact details to be changed
            cancelChangesBtn.setVisible(true);cancelChangesBtn.setEnabled(true);
            changeContactDetailsBtn.setText("Save Changes");
            areContactDetailsBeingEdited=true;
            phoneNumber=phoneNumberTxt.getText();email=emailTxt.getText();
            phoneNumberTxt.setEditable(true);
            emailTxt.setEditable(true);
        }
    }//GEN-LAST:event_changeContactDetailsBtnActionPerformed

    private void cancelChangesBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelChangesBtnActionPerformed
        // cancels changes made and exits editing mode
        areContactDetailsBeingEdited=false;
        cancelChangesBtn.setVisible(false);cancelChangesBtn.setEnabled(false);
        changeContactDetailsBtn.setText("Change Contact Details");
        phoneNumberTxt.setEditable(false);
        emailTxt.setEditable(false);
        emailTxt.setText(email);phoneNumberTxt.setText(phoneNumber);//reverts contact details to their values before editing began
    }//GEN-LAST:event_cancelChangesBtnActionPerformed

