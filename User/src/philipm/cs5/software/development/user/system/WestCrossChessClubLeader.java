    private void createNewLeaderBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createNewLeaderBtnActionPerformed
        // appoints a new chess club leader
        //asks for comfirmation before making the appointment
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to make somebody else the leader? This will mean that you will no longer be the leader.", "Proceed?", JOptionPane.YES_NO_CANCEL_OPTION);
        if(confirm==JOptionPane.YES_OPTION){
            //appoints somebody else as the chess club leader
            String newLeader = JOptionPane.showInputDialog(this,"Please input the username of the new leader:");
            if(newLeader==null){//if the user pressses cancel on the input dialog boc, nothing should occur
                return;
            }
            try{
                //performs validation
                if(ThreadClient.isUserNameAlreadyTaken(newLeader)==false){//checks to see that the username chosen is actually valid
                    JOptionPane.showMessageDialog(this,"This user does not exist. Please input a real user.","Error",JOptionPane.OK_OPTION);
                    return;
                }
                if(newLeader.equals(session.getUsername())){//if the new leader is just the same as the current leader, nothing needs to occur
                    return;
                }
                ThreadClient.setUserAsClubLeader(newLeader);//sets the new leader
                session.setIsClubLeader(false);//updates the session data
                JOptionPane.showMessageDialog(this,"You are no longer the club leader.","Success",JOptionPane.OK_OPTION);
                Menu menu = new Menu(session);menu.setVisible(true);this.dispose();//takes the user back to the menu
                return;
            }catch(Exception e){
                JOptionPane.showMessageDialog(this,"Error when communicating with the server. "+e,"Error",JOptionPane.OK_OPTION);
                Menu menu = new Menu(session);menu.setVisible(true);this.dispose();
                return;
            }
        }else{
            return;
        }   
    }//GEN-LAST:event_createNewLeaderBtnActionPerformed

    private void viewLeagueTableBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewLeagueTableBtnActionPerformed
        //takes the leader to a form that allows them to veiw the current club league table
        try{
            String[][]leagueTable=ThreadClient.getLeagueTable();//requests the league table from the server
            if(leagueTable.length==0){
                //if the fixtures have not been generated yet, a list of all club members is requested from the server. The leader is then taken to the form
                String[]allPlayers=ThreadClient.getMembersOfChessClub();
                LeagueTable tab = new LeagueTable(session, allPlayers);
                tab.setVisible(true);this.dispose();return;
            }
            LeagueTable tab = new LeagueTable(session, leagueTable);
            tab.setVisible(true);this.dispose();return;
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Error when communicating with the server: "+e,"Error",JOptionPane.OK_OPTION);
            Menu menu = new Menu(session);menu.setVisible(true);this.dispose();
            return;
        }
    }//GEN-LAST:event_viewLeagueTableBtnActionPerformed

